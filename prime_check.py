def prime_check(*nums):
  known_prime = [2,3,5,7]

  for num in nums:
    if (num % 10 == 5 and num != 5) or num <= 1:
      print(f'{num}: Not Prime')
    elif num in known_prime:
      print(f'{num}: Yes Prime')
    elif num not in known_prime and num % 2 != 0 and num % 2 != 0 and num % 3 != 0 and num % 7 != 0:
      print(f'{num}: Yes Prime')
    else:
      print(f'{num}: Not Prime')

